public class _01_Type_Conversion {
    public static void main(String[] args) {
        byte b = 8;
        int i = b;
        System.out.println(i);

        int ii=300;
        byte bb = (byte) ii;
        System.out.println(bb);

        char c = 'a';
        int iii = c;
        System.out.println(iii);

        float f = 1.009f;
        int iiii = (int) f;
        System.out.println(iiii);
    }
}
